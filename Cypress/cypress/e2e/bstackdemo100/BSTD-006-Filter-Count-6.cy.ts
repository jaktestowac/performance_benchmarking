describe('BSTD 006 - Filter Count', () => {
  it('Select different vendors and verify counts', () => {
    cy.visit('https://bstackdemo.com/')
    .get("span")
    .contains("Apple")
    .click()
    .get(".shelf-item")
    .should('have.length',9)
    .get("span")
    .contains("Apple")
    .click()
    .get("span")
    .contains("Samsung")
    .click()
    .get(".shelf-item")
    .should('have.length',7)
    .get("span")
    .contains("Samsung")
    .click()
    .get("span")
    .contains("Google")
    .click()
    .get(".shelf-item")
    .should('have.length',3)
    .get("span")
    .contains("Google")
    .click()
    .get("span")
    .contains("OnePlus")
    .click()
    .get(".shelf-item")
    .should('have.length',6)
  })
})
